path = 'path/models/InternVL-Chat-V1-5'
model = AutoModel.from_pretrained(
    path,
    torch_dtype=torch.bfloat16,
    load_in_8bit=True,
    low_cpu_mem_usage=True,
    use_flash_attn=True,
    trust_remote_code=True).eval()
tokenizer = AutoTokenizer.from_pretrained(path, trust_remote_code=True, use_fast=False)

# set the max number of tiles in `max_num`
def chat(img,prompt):
    pixel_values = load_image(img, max_num=12).to(torch.bfloat16).cuda()
    generation_config = dict(max_new_tokens=4096, do_sample=True)



    # single-image single-round conversation (单图单轮对话)
    question = prompt
    question = 'This image is closely related to the question'+question+'Please understand and analyze it based on the image and the question, and give the answer to the question'
    
    response = model.chat(tokenizer, pixel_values, question, generation_config)
    return response
